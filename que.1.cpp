#include <iostream>
using namespace std;
int main() {
   int a = 21;
   int b = 10;
   int c ;
   c = a + b;
   cout << " Addition is:" << c << endl ;
   
   c = a - b;
   cout << "substraction is :" << c << endl ;
   
   c = a * b;
   cout << "multiplication is:" << c << endl ;
   
   c = a / b;
   cout << "division is" << c << endl ;
   
   c = a % b;
   cout << "modulus is: :" << c << endl ;
   return 0;
}